<template>
  <div>
    <div class="container">
      <el-row class="wrap">
        <el-menu :default-active="defaultActive"
                 @select="selectSidebar"
                 theme="dark"
                 :collapse="sidebarCollapse"
                 class="sidebar-menu">
          <el-menu-item index="manage">
            <i class="el-icon-menu"></i>
            <span slot="title">博客管理</span>
          </el-menu-item>
          <el-menu-item index="new-article">
            <i class="el-icon-edit"></i>
            <span slot="title">新文章</span>
          </el-menu-item>
          <el-menu-item index="user">
            <i class="el-icon-information"></i>
            <span slot="title">用户</span>
          </el-menu-item>
          <el-menu-item index="logout" @click="logout">
            <i class="el-icon-close"></i>
            <span slot="title">退出</span>
          </el-menu-item>
        </el-menu>
        <div class="main">
          <header class="main-header">
            <div class="sidebar-toggle" @click="toggleSidebar" :class="{'sidebar-active': sidebarCollapse}">
              <i class="el-icon-caret-left"></i>
            </div>
            <el-breadcrumb separator="/" class="breadcrumb">
              <el-breadcrumb-item class="breadcrumb-item"
                                  v-for="(item,index) in levelList"
                                  :key="index" :to="{path: item.path}">{{item.name}}</el-breadcrumb-item>
            </el-breadcrumb>
            <span class="title">Sleepwalker</span>
          </header>
          <div class="main-container">
            <router-view></router-view>
            <div class="admin-content" v-if="adminContent">
              <img :src="adminPic" alt="首页展示图片" class="admin-pic">
            </div>
          </div>
        </div>
      </el-row>
    </div>
  </div>
</template>
<script>
  import $ from 'jquery'
  export default {
    data () {
      return {
        sidebarCollapse: false,
        levelList: null,
        adminPic: require('../../static/img/bench_gif.gif') // static$ alias
      }
    },
    computed: {
      defaultActive () {
        return this.$route.path.split('/')[2]
      },
      adminContent () {
        return this.$route.name === 'admin'
      }
    },
    methods: {
//      showAdminContent () {
//        if (this.$route.name === 'admin') {
//          this.adminCotent = true
//        }
//      },
      toggleSidebar () {
        this.sidebarCollapse = !this.sidebarCollapse
        if (this.sidebarCollapse) {
          $('.main').stop().animate(
            {
              'margin-left': 64
            },
            {
              duration: '100',
              easing: 'swing'
            })
        } else {
          $('.main').stop().animate(
            {
              'margin-left': 150
            },
            {
              duration: 'fast',
              easing: 'swing'
            })
        }
      },
      selectSidebar (index, indexPath) {
        if (index !== 'logout') {
          this.$router.push({name: index})
        }
      },
      logout () {
        this.$store.dispatch('FedLogoutAction')
          .then(() => {
            this.$router.replace({name: 'login'})
            this.$message({
              type: 'success',
              message: '成功退出!'
            })
          })
      },
      getBreadCurmb () {
        let matched = this.$route.matched.filter((item) => {
          return item.name
          // TODO switch 将英文name转为中文?
        })
        console.log('match', matched)
        this.levelList = matched
      }
    },
    watch: {
      $route () {
        this.getBreadCurmb()
      }
    },
    mounted () {
      this.getBreadCurmb()
    }
  }
</script>

